    private void jbtnAddCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAddCustomerActionPerformed

        String message="";
        boolean allvalid=true;
        if(jcbCustomerID.getSelectedIndex()==0){
            allvalid=false;
            message +="Please choose any CustomerID\n\n";
        } 
        if(jcbRoomType.getSelectedIndex()==0){
            allvalid=false;
            message +="Please choose any Room type\n\n";
        }
        String NumberofPeople =jtfNoofPeople.getText().trim();
        String NumberofRooms = jtfNoofRooms.getText().trim();
        String NumberofDays = jtfNoofDays.getText().trim();
        if(Validations.isEmpty(NumberofPeople)||Validations.isEmpty(NumberofRooms)||Validations.isEmpty(NumberofDays))
        {
            allvalid=false;
            message +="Please fill all boxes\n\n";
        }
        if(allvalid){
            BookingInfoTO rec = new BookingInfoTO();
            CustomerInfoTO cus = (CustomerInfoTO)jcbCustomerID.getSelectedItem();
            RoomTypeTO room = (RoomTypeTO)jcbRoomType.getSelectedItem();
            rec.setNoofdays(Integer.parseInt(NumberofDays));
            rec.setNoofpeople(Integer.parseInt(NumberofPeople));
            rec.setTotalrooms(Integer.parseInt(NumberofRooms));
            rec.setTypeID(room.getTypeID());
            rec.setCustomerid(cus.getCustomerid());
            BookingInfoDAO ac =new BookingInfoDAO();
           
            if(ac.insertRecord(rec)){
                message ="Welcome to our hotel!!!We wish you will like it!!";
            }else{
                message="Insertion failure"+ ac.getError_message();
            }
        }
        JOptionPane.showMessageDialog(this, message);
    }//GEN-LAST:event_jbtnAddCustomerActionPerformed


