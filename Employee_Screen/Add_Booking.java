     private void bindCustomerInfoCombo(){
    cus = new CustomerInfoDAO().getallRecord();
    jcbCustomerID.removeAllItems();
    jcbCustomerID.addItem("Choose any one type");
    if(cus!=null && cus.size()>0){
        for(CustomerInfoTO cus:cus){
            jcbCustomerID.addItem(cus);
        }
    }
}
